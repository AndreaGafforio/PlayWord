
let renewed = document.querySelector(".reset");


function Palreset(){
	
let Uinput = document.querySelector("#input");
let result = document.querySelector("#result");
let wrong = document.querySelector(".Error");
	
Uinput.value = "";
result.value = "";
wrong.innerHTML = "";	
	
	
return 0;	
}

renewed.addEventListener('click', Palreset);

