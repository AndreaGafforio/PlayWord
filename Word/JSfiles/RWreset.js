
let RWrenew = document.querySelector("#RWreset");

function RWordReset(){
	
let word = document.querySelector("#demo");
let result = document.querySelector("#result");
let wrong = document.querySelector(".Error");
	
word.value = "";
result.value = "";
wrong.innerHTML = "";	
	
	
return 0;	
}

RWrenew.addEventListener('click', RWordReset);